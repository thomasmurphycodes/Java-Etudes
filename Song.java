public class Song {
	
	public String name;
	public int bpm;
	public int length;
	public String label;
	public String[] remixers;

	public Song(String name, int bpm, int length, String label, String[] remixers){
		name = name;
		bpm = bpm;
		length = length;
		label = label;
		remixers = remixers;
	}
}